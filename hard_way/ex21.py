def add(a, b):
  print(f"Adding {a} + {b}")
  return a + b

def subtract(a, b):
  print(f"Subtracting {a} - {b}")
  return a - b

def multiply(a, b):
  print(f"Multiplying {a} x {b}")
  return a * b

print("Let's do some math with just functions!")

age = add(30, 5)
print(f"Age: {age}")

height = subtract(78, 4)
print(f"Hight: {height}")

weight = multiply(90, 2)
print(f"Weight: {weight}")